//this is to calculate sum of two integers and float values as well
#include <iostream>
using namespace std;
int main()
{
   int a,b;
   float x,y;
   cout<<"Enter integer values a and b:"<<endl;
   cin>>a;
   cin >>b;
   cout<<"Enter float values x and y:"<<endl;
   cin>>x;
   cin >>y;
   cout<<"The sum of two integer values "<<a<<" and "<<b<<" is "<<a+b<<endl;
   cout<<"The sum of two float values "<<x<<" and "<<y<<" is "<<x+y;
    return 0;
}
